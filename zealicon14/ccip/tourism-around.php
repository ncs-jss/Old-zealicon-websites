                                            <h5><a href="#"> Red Fort </a></h5>
                                            <p>The Red sandstone walls of the massive <strong>Red Fort</strong> (Lal Qila) rise 33-m above the clamour of Old Delhi as a reminder of the magnificent power and pomp of the Mughal emperors. The walls, built in 1638, were designed to keep out invaders, now they mainly keep out the noise and confusion of the city.
</p><p>
The main gate, Lahore Gate, is one of the emotional and symbolic focal points of the modern Indian nation and attracts a major crowd on each Independence Day.
The vaulted arcade of Chatta Chowk, a bazaar selling tourist trinkets, leads into the huge fort compound. Inside is a veritable treasure trove of buildings, including the Drum House, the Hall of Public Audiences, the white marble Hall of Private Audiences, the Pearl Mosque, Royal Baths and Palace of Color.<br/><br/>
