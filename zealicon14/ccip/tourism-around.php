</p><p>
The highly decorative mosque has three great gates, four towers and two 40 m-high minarets constructed of strips of red sandstone and white marble. Travellers can hire robes at the northern gate. This may be the only time you get to dress like a local without feeling like an outsider so make the most of it.<br/><br/>
