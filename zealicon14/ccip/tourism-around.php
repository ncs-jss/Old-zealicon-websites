                                            <h5><a href="#"> India Gate </a></h5>
                                            <p>At the centre of New Delhi stands the 42 m high <strong>India Gate</strong>, an <strong>"Arc-de-Triomphe"</strong> like archway in the middle of a crossroad. Almost similar to its French counterpart, it commemorates the 70,000 Indian soldiers who lost their lives fighting for the British Army during the World War I. The memorial bears the names of more than 13,516 British and Indian soldiers killed in the Northwestern Frontier in the Afghan war of 1919.
</p><p>
The foundation stone of <strong>India Gate</strong> was laid by His Royal Highness, the Duke of Connaught in 1921 and it was designed by Edwin Lutyens. The monument was dedicated to the nation 10 years later by the then Viceroy, Lord Irwin. Another memorial, Amar Jawan Jyoti was added much later, after India got its independence. The eternal flame burns day and night under the arch to remind the nation of soldiers who laid down their lives in the Indo-Pakistan War of December 1971.<br/>
                                            </p>
