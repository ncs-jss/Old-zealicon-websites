                                            <h5><a href="#"> Taj Mahal </a></h5>
                                            <p>A white marble tomb built in 1631 - 48 in Agra, seat of the Mughal Empire, by Shah Jehan for his wife, Arjuman Banu Begum, the monument sums up many of the formal themes that have played through Islamic architecture. Its refined elegance is a conspicuous contrast both to the Hindu architecture of pre-Islamic India, with its thick walls, corbelled arches and heavy lintels, and to the Indo-Islamic styles, in which Hindu elements are combined with an eclecticassortment of motifs from Persian and Turkish sources.</p><p>
The<strong> Taj Mahal</strong> is considered the finest example of Mughal architecture, a style that combines elements from Persian, Indian, and Islamic architectural styles. In 1983, the <strong>Taj Mahal</strong> became a UNESCO World Heritage Site and was cited as "the jewel of Muslim art in India and one of the universally admired masterpieces of the world's heritage."<br/><br/>
                                            </p>
