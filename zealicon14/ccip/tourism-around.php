                                            <h5><a href="#"> Qutab Minar </a></h5>
                                            <p><strong>Qutab Minar</strong> is a soaring, 73 m-high tower of victory, built in 1193 by Qutab-ud-din Aibak immediately after the defeat of Delhi's last Hindu kingdom. The tower has five distinct storeys, each marked by a projecting balcony and tapers from a 15 m diameter at the base to just 2.5 m at the top. The first three storeys are made of red sandstone; the fourth and fifth storeys are of marble and sandstone.At the foot of the tower is the Quwwat-ul-Islam Mosque, the first mosque to be built in India.</p>
<p>  An inscription over its eastern gate provocatively informs that it was built with material obtained from demolishing '27 Hindu temples'. A 7 m-high iron pillar stands in the courtyard of the mosque. It is said that if you can encircle it with your hands while standing with your back to it your wish will be fulfilled.The main mosque comprises of an inner and outer courtyard,decorated with shafts and surrounded by piller. Most of these shafts are from the 27 Hindu temples, which were plundered to construct the mosque. It is, therefore, not surprising that the Muslim mosque has typical Hindu ornamentation. 
                                            </p>
